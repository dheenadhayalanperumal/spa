    <script>
      $("#calendar").fullCalendar({
        header: {
          left: "prev,next",
          center: "title",
          right: "month",
        },
        editable: false,
        eventLimit: true, // Allow "more" link when too many events
        events: function (start, end, timezone, callback) {
          $.ajax({
            url: "ajax/f_event.php",
            type: "GET",
            data: {
              start: start.format(),
              end: end.format(),
            },
            success: function (response) {
              var events = JSON.parse(response);
              callback(events); // This prevents duplication
            },
          });
        },
        eventClick: function (event) {
          if (event.id) {
            $.ajax({
              type: "post",
              url: "Ajax/get_appdata.php",
              data: {
                id: event.id,
              },
              dataType: "json",
              success: function (response) {
                console.log(response);

                Swal.fire({
                  title: "",

                  html: `
    customer name :<b>${response.customer_name}</b>
    <br>
    mobile_number :<b>${response.mobile_number}</b>
       <br>
    email : <b>${response.email}</b>
       <br>
    interested services : <b>${response.interested_services}</b>
       <br>
    preferred time : <b>${response.preferred_time}</b>
       <br>
    source : <b>${response.source}</b>
       <br>
    enquiry status : <b>${response.enquiry_status}</b>
       <br>

  `,
                  showCloseButton: true,
                  showCancelButton: false,
                  focusConfirm: false,
                });
              },
            });
          }
        },
      });
